<template lang="pug">
div(class="nu-tmp"
    :style="styles()")
  nu-layer(v-for="(layer,index) in config.layers"
    :key="`layer-${index}`"
    :pageIndex="pageIndex"
    :page="page"
    :layerIndex="layerIndex"
    :subLayerIndex="index"
    :contentScaleRatio="contentScaleRatio"
    :config="layer"
    :style="subLayerStyles(layer)"
    :isSubLayer="true"
    :inTmp="true"
    :primaryScale="config.styles.scale"
    :primaryLayer="config"
    :inPreview="inPreview")
</template>

<script lang="ts">
import { ILayer, ITmp } from '@nu/vivi-lib/interfaces/layer'
import { IPage } from '@nu/vivi-lib/interfaces/page'
import layerUtils from '@nu/vivi-lib/utils/layerUtils'
import pageUtils from '@nu/vivi-lib/utils/pageUtils'
import textPropUtils from '@nu/vivi-lib/utils/textPropUtils'
import { PropType, defineComponent } from 'vue'

export default defineComponent({
  emits: [],
  props: {
    config: {
      type: Object,
      required: true
    },
    pageIndex: {
      type: Number,
      required: true
    },
    page: {
      type: Object as PropType<IPage>,
      required: true
    },
    layerIndex: {
      type: Number,
      required: true
    },
    contentScaleRatio: {
      default: 1,
      type: Number
    },
    inPreview: {
      default: false,
      type: Boolean
    }
  },
  created() {
    for (const [idx, layer] of (this.config as ITmp).layers.entries()) {
      if (layer.type === 'text') {
        layerUtils.updateSelectedLayerProps(this.pageIndex, idx, { editing: false })
      }
    }
    textPropUtils.updateTextPropsState()
  },
  computed: {
    transformStyle(): { [index: string]: string } {
      return {
        transformStyle: this.enalble3dTransform ? 'preserve-3d' : 'initial'
      }
    },
    enalble3dTransform(): boolean {
      return this.pageIndex === pageUtils._3dEnabledPageIndex
    }
  },
  methods: {
    styles() {
      return {
        ...this.transformStyle
      }
    },
    subLayerStyles(layer: ILayer) {
      const primaryScale = this.config.styles.scale
      return (layer.type === 'shape' && layer.category === 'D') ? {} : { outline: `${2 / primaryScale}px solid #9C9C9C` }
    }
  }
})
</script>

<style lang="scss" scoped>
.nu-tmp {
  position: absolute;
}
</style>
